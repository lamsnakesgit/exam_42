#include <unistd.h>

int main(int  ac, char **av)
{
	int i;

	i = -1;
	if (ac == 2)
	{
		while (av[1][++i] && av[1][i] == ' ');
	/*	if (av[1][i] == ' ')
			while (av[1][i + 1])
				write(1, &(av[1][i + 1]), 1);*/
		if (!(av[1][0]))
		{
			write(1, "\n", 1);
			return (0);
		}
		while (av[++i]);
		while (av[1][--i] && av[1][i] == ' ' && i > 0/* ? */);
		if (av[1][i] && av[1][i/* - 1*/] != ' ')
			av[1][i + 1] = '\n';
	}
	else
		write(1, "\n", 1);
	return (0);
}
