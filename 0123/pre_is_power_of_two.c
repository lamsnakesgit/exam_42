int	    is_power_of_2(unsigned int n)
{
	while (n % 2 == 0)
		n /= 2;
	return (n == 1 ? 1 : 0);//(n % 2 == 1 ? 0 : 1);
}
#include <stdio.h>
int			main()
{
	int n = 3;

	printf("ip2=%d\n", is_power_of_2(n));
	return (0);
}
