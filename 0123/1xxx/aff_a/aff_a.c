#include <unistd.h>
int main (int argc, char **argv)
{
	int i;
	int j;

	j = 0;
	if (argc == 2)
	{
			while(argv[1][j] != 'a' && argv[1][j] != '\0')
				j++;
			if (argv[1][j] == '\0')
//				if (argv[1][j - 1] = 'a')
				write(1, "\n", 1);
			else if (argv[1][j] == 'a')
				write(1, "a\n", 2);
	}
	else
		write(1, "a\n", 2);
}
