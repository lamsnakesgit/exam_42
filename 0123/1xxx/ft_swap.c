#include <stdio.h>
void		ft_swap(int *a, int *b)
{
	*a = *a + *b;
	*b = *a - *b;
	*a = *a - *b;
//	printf("%d", &a, &b);
}

int			main(void)
{
	int a;
	int b;

	a = 9;
	b = 17;
	ft_swap(&a, &b);
	printf("%d%d", a, b);
	return (0);
}
